    @property
    def headers(self):
        fake_headers = {
            'Accept': 'text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8',
            'Accept-Language': 'en-US,en;q=0.5',
            'Connection': 'keep-alive',
            'Accept-Encoding': 'gzip, deflate',
            'User-Agent': self.user_agent,
        }
        return fake_headers

    def get(self, url, timeout=15, interval=1, **kwargs):
        headers = self.headers
        h = kwargs.get('headers')
        if h and isinstance(h, dict):
            headers.update(h)
            kwargs.pop('headers')

        # TODO add config
        if self.retry_time > 5:
            logger.info('{} retry time > 5, return blank response'.format(url))
            res = Response()
            return res

        try:
            res = requests.get(url, headers=headers, timeout=timeout, **kwargs)
            time.sleep(interval)
        except Exception as e:
            time.sleep(interval)
            self.retry_time += 1
            logger.debug('request {} fail'.format(url))
            return self.get(url, timeout=timeout, interval=interval, **kwargs)

        return res
